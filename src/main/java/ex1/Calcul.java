package ex1;

/** Classe de différentes opérations.
 *
 */
public class Calcul {

	/** Méthode permettant de réaliser une addition entre deux parametre
	 *
	 * @param a
	 * @param b
	 * @return
	 */
	public int addition(int a, int b){
		return a+b;
	}

	/** Méthode permettant de réaliser une soustraction entre deux parametre
	 *
	 * @param a
	 * @param b
	 * @return
	 */
	public int soustraction(int a, int b){
		return a-b;
	}
}
